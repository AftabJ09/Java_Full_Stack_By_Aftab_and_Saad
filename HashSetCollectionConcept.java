import java.util.HashSet;
import java.util.Set;
public class HashSetCollectionConcept{
    public static void main(String[] args){
        Set<Integer> set = new HashSet<>();
        set.add(1);
        System.out.println(set);
        set.add(3);
        System.out.println(set);
        set.add(7);
        System.out.println(set);

    }
}
